      SUBROUTINE SICP(CXX,COEF,OSD,L1,N,MO)
C     THIS SUBROUTINE FITS AUTOREGRESSIVE MODELS
C     X(N)=A(1)X(N-1)+...+A(M)X(N-M)+E(N)
C     OF SUCCESSIVELY INCREASING ORDER UP TO L(=L1-1).
C     INPUT:
C     CXX(I),I=0,L1; AUTOCOVARIANCE SEQUENCE
C     L1; L1=L+1, L IS THE UPPER LIMIT OF THE MODEL ORDER
C     N; LENGTH OF ORIGINAL DATA
C     OUT PUT:
C     MO; ORDER OF AR
C     OSD; INNOVATION VARIANCE
C     COEF; AR-COEFFICIENTS
      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION CXX(L1),COEF(L1)
cc	DIMENSION A(500),B(500)
      DIMENSION A(L1-1),B(L1-1)
      CST1=1.0D-00
      CST2=2.0D-00
      L=L1-1
      SD=CXX(1)
      AN=N
      OAIC=AN*DLOG(SD)
      OSD=SD
      MO=0
      SE=CXX(2)
C     ITERATION START
      DO 400 M=1,L
      MP1=M+1
      D=SE/SD
      A(M)=D
      D2=D*D
      SD=(CST1-D2)*SD
      AM=M
      AIC=AN*DLOG(SD)+CST2*AM
      DLSD=DLOG(SD)
      IF(M.EQ.1) GO TO 410
C
C     A(I) COMPUTATION
      LM=M-1
      DO 420 I=1,LM
  420 A(I)=A(I)-D*B(I)
  410 DO 421 I=1,M
      IM=MP1-I
  421 B(I)=A(IM)
C
C     M,SD,FICP PRINT OUT
C     WRITE(6,860) M
C     WRITE(6,861) SD,FICP
C     WRITE(6,960)
C     DO 961 I=1,M
C 961 WRITE(6,662) I,A(I)
C
      IF(OAIC.LT.AIC) GO TO 440
      OAIC=AIC
      OSD=SD
      MO=M
      DO 430 I=1,M
  430 COEF(I)=A(I)
  440 IF(M.EQ.L) GO TO 400
      SE=CXX(M+2)
      DO 441 I=1,M
  441 SE=SE-B(I)*CXX(I+1)
  400 CONTINUE
  699 RETURN
